class BlockLetters {
    public static void main(String[] args) {
        printH();
        printE();
        printL();
        printL();
        printO();
    }



    
        static void printH() {
        System.out.println("*   *");
        System.out.println("*   *");
        System.out.println("*****");
        System.out.println("*   *");
        System.out.println("*   *");
        System.out.println();
        }
        static void printE() {
        System.out.println("*****");
        System.out.println("*");
        System.out.println("***");
        System.out.println("*");
        System.out.println("*****");
        System.out.println();
        }
        static void printL() {
        System.out.println("*");
        System.out.println("*");
        System.out.println("*");
        System.out.println("*");
        System.out.println("*****");
        System.out.println();
        }
static void printO() {
        System.out.println();
        System.out.println("*****");
        System.out.println("*   *");
        System.out.println("*   *");
        System.out.println("*   *");
        System.out.println("*****");
    }
}